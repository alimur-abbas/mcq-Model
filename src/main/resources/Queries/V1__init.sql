CREATE TABLE TBL_USER_ATTEMPT(
USER_ACCOUNT_ID VARCHAR(256) NOT NULL,
ATTEMPT_ID VARCHAR(256) PRIMARY KEY,
EXAM_ID VARCHAR(256) NOT NULL,
EXAM_TYPE VARCHAR(256) NOT NULL,
STARTED_AT LONG NOT NULL,
END_AT LONG NOT NULL,
RESULT TEXT NOT NULL,
SCORE INT NOT NULL
);